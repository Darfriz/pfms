    <h3>Research Title: Personal Financial Management System</h3><br>

    <h4>Introduction of Research</h4>
<p>Financial problems can hurt one’s life and happiness. <br>
    Therefore, there is a need for a web application that can help working individuals to manage and organize their personal finances efficiently. <br>
    The web app should have a user-friendly and responsive interface that lets users do various financial tasks and features, <br>
    such as record their liquid and non-liquid assets, liabilities, monthly commitment, set their financial goals and budgets, <br>
    calculate Debt Service Ratio (DSR), generate, and visualize their financial performance and access their financial data from a browser, and many more. <br>
    The web app should also help users improve their financial awareness, make informed financial decisions, and avoid common financial mistakes. <br>
    The web app should help users reach their financial goals and improve their financial well-being. <br>
    Therefore, the objective of this project is to develop centralized web app platform for personal financial management. <br>
</p><br>
<h4>Purpose of Research</h4>
The purpose of this research is to investigate the use of centralized personal financial management system for any working <br> individuals as well as pensioner/retiree. <br>
